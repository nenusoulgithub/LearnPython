# int() float() str()
print(int("123"))
print(float("123.321"))
print(str(123))

# bin() 十进制转二进制
print(bin(4))

# hex() 十进制转16进制
print(hex(15))

# list() 转化为列表
li = list((1, 2, 3))
li.append(4)
print(li)

# tuple() 转化为元组
t = tuple([1, 2, 3])
print(t)

# dict() 转化为字典
dic = dict(name="小明", age=18)
print(dic)

# bytes() 转化字节
print(bytes("我喜欢Python", "UTF8"))

