# + - * / % ** // 算数运算符
# 定义两个变量
a = 7
b = 3
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)

# == != > < >= <= 比较运算符
a, b, c = 10, 5, 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a > b < c)
